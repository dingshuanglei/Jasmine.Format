namespace Jasmine.Format.Utilities
{
    /// <summary>
    /// Provides constants for HTML tag names.
    /// </summary>
    internal static class HtmlTagNames
    {
        /// <summary>
        /// The span tag name.
        /// </summary>
        public const string Span = "span";

        /// <summary>
        /// The anchor (link) tag name.
        /// </summary>
        public const string A = "a";

        /// <summary>
        /// The paragraph tag name.
        /// </summary>
        public const string P = "p";

        /// <summary>
        /// The div tag name.
        /// </summary>
        public const string Div = "div";

        /// <summary>
        /// The unordered list tag name.
        /// </summary>
        public const string Ul = "ul";

        /// <summary>
        /// The ordered list tag name.
        /// </summary>
        public const string Ol = "ol";

        /// <summary>
        /// The list item tag name.
        /// </summary>
        public const string Li = "li";

        /// <summary>
        /// The table tag name.
        /// </summary>
        public const string Table = "table";

        /// <summary>
        /// The table row tag name.
        /// </summary>
        public const string Tr = "tr";

        /// <summary>
        /// The table header cell tag name.
        /// </summary>
        public const string Th = "th";

        /// <summary>
        /// The table data cell tag name.
        /// </summary>
        public const string Td = "td";

        /// <summary>
        /// The image tag name.
        /// </summary>
        public const string Img = "img";

        /// <summary>
        /// The audio tag name.
        /// </summary>
        public const string Audio = "audio";

        /// <summary>
        /// The video tag name.
        /// </summary>
        public const string Video = "video";

        /// <summary>
        /// The source tag name (for media elements).
        /// </summary>
        public const string Source = "source";

        /// <summary>
        /// The table head tag name.
        /// </summary>
        public const string THead = "thead";

        /// <summary>
        /// The table body tag name.
        /// </summary>
        public const string TBody = "tbody";
    }
}